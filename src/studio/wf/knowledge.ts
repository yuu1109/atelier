/** 主要セクション種の辞書（tools/web/data.ts の SECTION_MAP から整形） */
function buildSectionDictionary(): string {
  const lines = WEB_SECTIONS
    // ヘッダー・フッターはWfPlanの構造（header/footer）側で扱うので辞書から除く
    .filter((s) => s.id !== "header-nav" && s.id !== "footer")
    .map((s) => `- ${s.id}（${s.name}）: ${s.purpose}`);
  return `## セクション種の辞書（WfSection.kind に使うid）

${lines.join("\n")}

この辞書に無い構造が必要なら kind: "custom" を使ってよい。辞書も型もたたき台であり、ユーザーの文脈（業種・強み・目的）に合わせて自由に構成してよい。`;
}

